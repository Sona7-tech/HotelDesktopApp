    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        
       try {
           
           Long cardNumber = Long.parseLong(cardNumberTxt.getText());
           Double amount = Double.parseDouble(amountTxt.getText());
           PaymentOpt paymentOpt = (PaymentOpt) paymentOptionCmb.getSelectedItem(); 
           Payment payment = paymentDao.getPaymentById(selectedId);
           payment.setCardNumber(cardNumber);
           payment.setTotalAmount(amount);
           payment.setPaymentOpt(paymentOpt);
           paymentDao.updatePayment(payment);
           JOptionPane.showMessageDialog(null, "Payment hass been succesfully updated!");
           
       } catch (Exception ex) {
           ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Problem! Payment has not been successfully updated!","Error",JOptionPane.ERROR);
       }
       this.dispose();
       try {
           aThis.showPaymentData(paymentDao.getPaymentList());
       } catch (Exception ex) {
          ex.printStackTrace();
       }
       
      
    }//GEN-LAST:event_updateBtnActionPerformed


    private void editOptionCombo(PaymentOpt p) {
        try {
           DefaultComboBoxModel optionCombo = (DefaultComboBoxModel) paymentOptionCmb.getModel();
           List<PaymentOpt> paymentOptlist = paymentOptDao.getPaymentOptList();
           for(PaymentOpt paymentOpt: paymentOptlist){
               optionCombo.addElement(paymentOpt);
           }
           optionCombo.setSelectedItem(p);
       } catch (Exception ex) {
          ex.printStackTrace();
       }
    }

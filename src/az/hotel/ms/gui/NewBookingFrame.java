    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
      try{
      Client client = (Client) clientCmb.getSelectedItem();
      Room room = (Room) roomNumberCmb.getSelectedItem();
      Date checkIn =  checkInCmb.getDate();
      Date checkOut = checkOutCmb.getDate();
      Booking booking = new Booking();
      booking.setClient(client);
      booking.setRoom(room);
      booking.setCheckIn(checkIn);
      booking.setCheckOut(checkOut);
      bookingDao.addBooking(booking);
      JOptionPane.showMessageDialog(null,"Booking has been successfully created!");
      
      } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Problem! Booking has not been successfully created!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
      this.dispose();
        try {
            aThis.showBookingData(bookingDao.getBookingList());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
      
    }//GEN-LAST:event_saveBtnActionPerformed


    private void showRoomCombo() {
        
        try {
            DefaultComboBoxModel roomCombo = (DefaultComboBoxModel) roomNumberCmb.getModel();
            
            List<Room> roomList = roomDao.getRoomList();
            for(Room room: roomList){
                
                roomCombo.addElement(room);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showClientCombo() {
        
        try {
            DefaultComboBoxModel clientCombo = (DefaultComboBoxModel) clientCmb.getModel();
            
            List<Client> clientList = clientDao.getClientList();
            for(Client client: clientList){
                
                clientCombo.addElement(client);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }

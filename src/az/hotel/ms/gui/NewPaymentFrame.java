    private void showBookingCombo() {
       
        try {
            DefaultComboBoxModel bookingCombo = (DefaultComboBoxModel) clientCmb.getModel();
            
            //DefaultComboBoxModel menuuCombo = (DefaultComboBoxModel) menuDescCmb.getModel();
            List<Booking> bookingList = bookingDao.getBookingList();
            for(Booking booking: bookingList){
                
                bookingCombo.addElement(booking);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showPaymentOptCombo() {
       
         try {
            DefaultComboBoxModel optionCombo = (DefaultComboBoxModel) optionCmb.getModel();
            
            List<PaymentOpt> paymentOptList = paymentOptDao.getPaymentOptList();
            for(PaymentOpt paymentOpt: paymentOptList){
                
                optionCombo.addElement(paymentOpt);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

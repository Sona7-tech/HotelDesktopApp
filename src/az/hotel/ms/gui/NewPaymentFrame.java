    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Booking booking =  (Booking) clientCmb.getSelectedItem();
            Long cardNumber = Long.parseLong(cardTxt.getText());
            Double totalAmount = Double.parseDouble(amountTxt.getText());
            PaymentOpt paymentOpt = (PaymentOpt) optionCmb.getSelectedItem();
            Payment payment = new Payment();
            payment.setBooking(booking);
            payment.setCardNumber(cardNumber);
            payment.setTotalAmount(totalAmount);
            payment.setPaymentOpt(paymentOpt);
            paymentDao.addPayment(payment);
            JOptionPane.showMessageDialog(null,"Payment has been successfully created!");
             
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Problem! Payment has not been successfully created!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
       this.dispose();
        try {
            aThis.showPaymentData(paymentDao.getPaymentList());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed


